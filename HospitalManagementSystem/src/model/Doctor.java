package model;

/**
 *
 * @author user
 */
public class Doctor {

    private int id;
    private String name;
    private String gender;
    private int departmentId;
    private int salary;

    public Doctor() {
    }

    public Doctor(int id, String name, String gender, int departmentId, int salary) {
        this.id = id;
        this.name = name;
        this.gender = gender;
        this.departmentId = departmentId;
        this.salary = salary;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public int getDepartmentId() {
        return departmentId;
    }

    public void setDepartmentId(int departmentId) {
        this.departmentId = departmentId;
    }

    public int getSalary() {
        return salary;
    }

    public void setSalary(int salary) {
        this.salary = salary;
    }

}
